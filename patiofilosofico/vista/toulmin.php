<p class="textos">
    
    Antes de discutir en detalle el modelo de Toulmin, resulta pertinente introducir las nociones de argumento y argumentación que se emplearán en esta revisión: un argumento se refiere a los discursos que un estudiante o un grupo de estudiantes producen cuando deben articular o justificar sus conclusiones o explicaciones, mientras que la argumentación alude al proceso de elaboración de esos discursos (OSBORNE; ERDURAN; SIMON, 2004). <br><br>

Uno de los aspectos más interesantes del modelo de Toulmin ‒ designado habitualmente como TAP por las siglas en inglés de Toulmin's argument pattern ‒es que ofrece un potente enfoque para estudiar lo que el filósofo británico denomina argumentos sustantivos, es decir, aquellos que deben ser examinados atendiendo a su contenido, lo cual marca una profunda diferencia con la tradición aristotélica, que se interesa únicamente por la forma o estructura de un argumento. <br><br>

Es entonces cuando Toulmin propone que las argumentaciones cotidianas no siguen el clásico modelo riguroso del silogismo y crea uno adecuado para analizar cualquier tipo de argumentación en el marco de los discursos sociales: conversación, periódico, televisión, radio, prensa escrita, entrevista, interacción docente alumno, médico-paciente, abogado-cliente. Considera que un “argumento” es una estructura compleja de datos. <br><br>

Toulmin plantea que un argumento sustantivo va desde los datos (D) a la conclusión (C), donde D corresponde a la información, antecedentes o hechos de los cuales disponemos para dar fundamento a C. TAP también incorpora la garantía (G), el respaldo (B), el calificador modal (Q) y las condiciones de refutación (R). Además, Toulmin supone que un argumento propiamente dicho consiste en al menos tres componentes esenciales: D, C y G. <br><br>

Las garantías (warrants) son las que permiten justificar el paso desde los datos (data) a la conclusión (claims), vale decir, G cumple la función de mostrar que el paso de D hacia C es adecuado y legítimo. El modelo incorpora explícitamente el grado de certeza (o incerteza) del argumento mediante el calificador modal Q (qualifier). Ejemplos de calificadores modales son expresiones como: siempre, a veces, probablemente, depende, etc. Además, la refutación (rebuttals) son condiciones que establecen las restricciones que se aplican a C, es decir, las situaciones bajo las cuales C no sería válida. Finalmente, el respaldo (backings) se refiere a las circunstancias generales bajo las cuales G es apoyada. <br><br>

En resumen, el esquema opera de la siguiente manera: A partir de una evidencia (datos) se formula una conclusión (proposición). Una garantía conecta los datos con la conclusión y se ofrece su cimiento teórico, práctico o experimental: el respaldo. Los calificadores modales (ciertamente, sin duda) indican el modo en que se interpreta la aserción como verdadera, contingente o probable. Finalmente, se consideran sus posibles refutaciones u objeciones. <br><br>

A continuación, se presenta una descripción esquemática de TAP aplicada a un ejemplo concreto acerca del Péndulo de Foucault, exhibido en numerosos museos a través del mundo, y cuyo funcionamiento permite concluir que nuestro planeta gira sobre su propio eje. Naturalmente, un argumento puede volverse bastante más complejo, e incluir varios datos, garantías, refutaciones, etc., pero este esquema muestra todos los componentes fundamentales. 
